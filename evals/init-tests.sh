#!/usr/bin/env bash
# Deterministic eval for opt #3 — no Claude, free. Two layers:
#  (1) the bin/claudehut-init SCRIPT writes the canonical plane + stack-gated rules + idempotent @import;
#  (2) the SessionStart FALLBACK (bootstrap.sh auto-running the script when the plane is absent) — which IS
#      the live INVOCATION path, but model-independent, so it is verifiable here (the model is not in that loop).
# P7 separately measured the skill's !`…` invocation as flaky (2/3); the bootstrap fallback is the reliable close.
# Run: evals/init-tests.sh   (see evals/FOLLOWUP-init-script.md §12)
set -uo pipefail
ROOT="$(cd "$(dirname "$0")/.." && pwd)"
INIT="$ROOT/bin/claudehut-init"
PASS=0; FAIL=0
ok(){ PASS=$((PASS+1)); echo "  ok   - $1"; }
bad(){ FAIL=$((FAIL+1)); echo "  FAIL - $1"; }
command -v jq >/dev/null 2>&1 || { echo "jq required"; exit 2; }

run_init() { local src="$1" w; w="$(mktemp -d)/work"; mkdir -p "$w"; cp -R "$src/." "$w/" 2>/dev/null; CLAUDE_PLUGIN_ROOT="$ROOT" "$INIT" "$w" >/dev/null 2>&1; echo "$w"; }
prov(){ head -1 "$1" 2>/dev/null | grep -q 'generated by claudehut-init\|ClaudeHut rule template'; }

echo "== plane generation + idempotency (clean-first-run) =="
W="$(run_init "$ROOT/evals/tasks/clean-first-run/repo")"
for f in MEMORY.md PROJECT.md LANGUAGE.md architecture.md reuse-index.json; do
  [ -f "$W/.claude/claudehut/$f" ] && ok "plane: $f written" || bad "plane: $f MISSING"
done
prov "$W/.claude/claudehut/MEMORY.md" && ok "provenance line present" || bad "no provenance line"
jq -e . "$W/.claude/claudehut/reuse-index.json" >/dev/null 2>&1 && ok "reuse-index.json valid JSON" || bad "reuse-index.json invalid"
grep -q '{{' "$W/.claude/claudehut/PROJECT.md" && bad "unsubstituted {{...}} in PROJECT.md" || ok "no unsubstituted tokens in PROJECT.md"
grep -q 'com\.example' "$W/.claude/claudehut/PROJECT.md" && ok "base package detected (com.example, single-file repo)" || bad "base package wrong"
[ -f "$W/.claude/claudehut/learnings.jsonl" ] && ok "learnings.jsonl created" || bad "learnings.jsonl missing"
echo seed > "$W/.claude/claudehut/learnings.jsonl"   # idempotency: must NOT be clobbered on re-run
CLAUDE_PLUGIN_ROOT="$ROOT" "$INIT" "$W" >/dev/null 2>&1   # second run
n=$(grep -c 'project-adaptive memory' "$W/CLAUDE.md" 2>/dev/null || echo 0)
[ "$n" = "1" ] && ok "@import idempotent (1 block after 2 runs)" || bad "@import not idempotent ($n blocks)"
[ "$(cat "$W/.claude/claudehut/learnings.jsonl")" = "seed" ] && ok "learnings.jsonl not clobbered on re-run" || bad "learnings.jsonl clobbered"
rm -rf "$W"

echo "== stack-gating (reactive-kafka: webflux + r2dbc + kafka + redis) =="
W="$(run_init "$ROOT/evals/tasks/_fixtures/reactive-kafka")"; R="$W/.claude/rules"
for want in framework/webflux.md framework/r2dbc.md framework/kafka-consumer.md framework/kafka-producer.md framework/redis.md performance/backpressure.md testing/stepverifier.md; do
  [ -f "$R/$want" ] && ok "emitted: $want" || bad "missing: $want"
done
for no in framework/spring-mvc.md framework/jpa.md framework/nats.md framework/rabbitmq.md; do
  [ -f "$R/$no" ] && bad "should NOT emit: $no" || ok "gated out: $no"
done
grep -q 'com\.acme\.app' "$W/.claude/claudehut/PROJECT.md" && ok "base package (com.acme.app)" || bad "base package wrong (reactive-kafka)"
rm -rf "$W"

echo "== stack-gating (servlet-jpa: mvc + jpa + postgres) =="
W="$(run_init "$ROOT/evals/tasks/_fixtures/servlet-jpa")"; R="$W/.claude/rules"
for want in framework/spring-mvc.md framework/jpa.md coding/naming.md security/owasp-top10.md; do
  [ -f "$R/$want" ] && ok "emitted: $want" || bad "missing: $want"
done
for no in framework/webflux.md framework/r2dbc.md framework/redis.md testing/stepverifier.md; do
  [ -f "$R/$no" ] && bad "should NOT emit: $no" || ok "gated out: $no"
done
rm -rf "$W"

echo "== --detect (servlet-jpa) =="
D="$(CLAUDE_PLUGIN_ROOT="$ROOT" "$INIT" "$ROOT/evals/tasks/_fixtures/servlet-jpa" --detect 2>/dev/null)"
echo "$D" | jq -e '.web=="mvc" and .orm=="jpa" and .db=="postgresql" and .base_package=="com.acme.web"' >/dev/null 2>&1 \
  && ok "--detect correct (web=mvc orm=jpa db=postgresql base=com.acme.web)" || bad "--detect wrong: $D"

echo "== fallback: bootstrap.sh (SessionStart) auto-generates the plane — opt #3 INVOCATION fix =="
# Faithfully replicates SessionStart: the hook pipes its JSON payload to bootstrap.sh. The model is NOT
# in the loop here, so this deterministically proves the fallback closes #3's invocation gap.
W="$(mktemp -d)/work"; mkdir -p "$W"; cp -R "$ROOT/evals/tasks/clean-first-run/repo/." "$W/"
echo '{"session_id":"p7fb","source":"startup"}' | CLAUDE_PROJECT_DIR="$W" CLAUDE_PLUGIN_ROOT="$ROOT" bash "$ROOT/scripts/bootstrap.sh" >/dev/null 2>&1
fp=0; for f in MEMORY.md PROJECT.md LANGUAGE.md architecture.md reuse-index.json; do [ -f "$W/.claude/claudehut/$f" ] && fp=$((fp+1)); done
[ "$fp" = 5 ] && ok "SessionStart fallback wrote the plane (5/5, zero model reliance)" || bad "fallback plane=$fp/5"
[ -f "$W/.claude/claudehut/state/p7fb.json" ] && ok "fallback also armed state (p7fb.json)" || bad "fallback did not arm state"
[ -d "$W/.claudehut" ] && bad "fallback created wrong-dir .claudehut/" || ok "fallback: no wrong-dir .claudehut/"
rm -rf "$W"

echo "== worktree.baseRef=head settings write (v0.5.0 — dependent-phase fan-out) =="
# create case: no settings.json -> init creates it with baseRef=head
W="$(run_init "$ROOT/evals/tasks/clean-first-run/repo")"
[ "$(jq -r '.worktree.baseRef // empty' "$W/.claude/settings.json" 2>/dev/null)" = "head" ] \
  && ok "init created .claude/settings.json with worktree.baseRef=head" || bad "settings.json baseRef not head"
# merge case: pre-existing settings keys must be PRESERVED, baseRef added
SRC="$(mktemp -d)/repo"; mkdir -p "$SRC/.claude"; cp -R "$ROOT/evals/tasks/clean-first-run/repo/." "$SRC/" 2>/dev/null
mkdir -p "$SRC/.claude"; printf '{"env":{"FOO":"bar"},"enabledPlugins":{"x":true}}' > "$SRC/.claude/settings.json"
W2="$(run_init "$SRC")"
b=$(jq -r '.worktree.baseRef // empty' "$W2/.claude/settings.json" 2>/dev/null)
foo=$(jq -r '.env.FOO // empty' "$W2/.claude/settings.json" 2>/dev/null)
[ "$b" = "head" ] && [ "$foo" = "bar" ] \
  && ok "init merges baseRef=head WITHOUT clobbering existing settings keys (env.FOO preserved)" \
  || bad "settings merge wrong (baseRef=$b env.FOO=$foo)"
# idempotent: second run keeps exactly one baseRef, still head
CLAUDE_PLUGIN_ROOT="$ROOT" "$INIT" "$W2" >/dev/null 2>&1
[ "$(jq -r '.worktree.baseRef' "$W2/.claude/settings.json" 2>/dev/null)" = "head" ] \
  && ok "settings baseRef idempotent on re-run" || bad "settings baseRef changed on re-run"
rm -rf "$W" "$W2"

echo; echo "INIT: $PASS passed, $FAIL failed"; [ "$FAIL" -eq 0 ]
